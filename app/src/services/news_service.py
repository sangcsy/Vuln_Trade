def stock_news_items(stock, limit=2):
    items = STOCK_NEWS_BY_SYMBOL.get(stock["symbol"], [])
    if not items:
        items = [
            {
                "title": "{name}, 현장 운영 프로세스 점검 착수",
                "summary": "고객 접점과 내부 승인 절차를 다시 살펴보며 불필요한 대기 시간을 줄이는 방안을 검토합니다.",
                "source": "Business Desk",
            },
            {
                "title": "{name}, 사내 아이디어 게시판에 현실적인 제안 증가",
                "summary": "회의 줄이기, 점심 메뉴 투표, 프린터 고장 알림처럼 바로 적용 가능한 안건이 좋은 반응을 얻고 있습니다.",
                "source": "Office Brief",
            },
        ]

    name = stock["name"]
    return [
        {
            "title": item["title"].format(name=name),
            "summary": item["summary"].format(name=name),
            "source": item["source"],
        }
        for item in items[:limit]
    ]
